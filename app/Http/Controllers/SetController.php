     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();
        $user = Auth::user();

        $set = Set::create([
          'name' => $input['name'],
          'description' => $input['description'],
          'user_id' => $user->id
        ]);

        return redirect()
        ->route('sets.index')
        ->with('status', 'Congratulations! Set was succesfully created.');
    }

